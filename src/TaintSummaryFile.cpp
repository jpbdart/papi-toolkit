std::string SummaryFileWriter::indent (int level)
{
    return std::string (level * 2, ' ');
}

std::string SummaryFileWriter::layerToYaml (TaintLayer layer)
{
    switch (layer)
        {
        case TaintLayer::RAW:
            return "RAW";
        case TaintLayer::SYNTACTIC:
            return "SYNTACTIC";
        case TaintLayer::SEMANTIC:
            return "SEMANTIC";
        case TaintLayer::CONTEXTUAL:
            return "CONTEXTUAL";
        case TaintLayer::CLEAN:
            return "CLEAN";
        }
    return "UNKNOWN";
}

std::string SummaryFileWriter::directionToYaml (ParamDirection dir)
{
    switch (dir)
        {
        case ParamDirection::IN:
            return "IN";
        case ParamDirection::OUT:
            return "OUT";
        case ParamDirection::INOUT:
            return "INOUT";
        }
    return "IN";
}

std::string modStatusToYaml (ParamModStatus status)
{
    switch (status)
        {
        case ParamModStatus::UNKNOWN:
            return "UNKNOWN";
        case ParamModStatus::PASS_THROUGH:
            return "PASS_THROUGH";
        case ParamModStatus::MODIFIED:
            return "MODIFIED";
        }
    return "UNKNOWN";
}

bool SummaryFileWriter::write (const std::string &filename,
                          const std::vector<FunctionSummary> &summaries,
                          const std::string &sourceFile)
{
    std::ofstream out (filename);
    if (!out.is_open ())
        {
            llvm::errs () << "Error: Cannot open file for writing: " << filename
                          << "\n";
            return false;
        }

    out << "# Taint Summary File\n";
    out << "# Generated by taint-analyzer\n";
    out << "---\n";
    out << "version: 2\n"; // Version bump for provenance support
    out << "source_file: " << sourceFile << "\n";
    out << "functions:\n";

    for (const auto &func : summaries)
        {
            out << indent (1) << "- name: " << func.name << "\n";
            out << indent (2) << "qualified_name: " << func.qualifiedName
                << "\n";
            out << indent (2)
                << "return_layer: " << layerToYaml (func.returnLayer) << "\n";
            out << indent (2) << "return_inherits: "
                << (func.returnInherits ? "true" : "false") << "\n";
            out << indent (2)
                << "return_inherit_source: " << func.returnInheritSource
                << "\n";
            out << indent (2) << "is_taint_source: "
                << (func.isTaintSource ? "true" : "false") << "\n";
            out << indent (2)
                << "is_taint_sink: " << (func.isTaintSink ? "true" : "false")
                << "\n";
            out << indent (2)
                << "sink_requirement: " << layerToYaml (func.sinkRequirement)
                << "\n";

            if (!func.params.empty ())
                {
                    out << indent (2) << "params:\n";
                    for (const auto &param : func.params)
                        {
                            out << indent (3) << "- index: " << param.index
                                << "\n";
                            if (!param.name.empty ())
                                {
                                    out << indent (4) << "name: " << param.name
                                        << "\n";
                                }
                            out << indent (4) << "direction: "
                                << directionToYaml (param.direction) << "\n";
                            out << indent (4) << "required_layer: "
                                << layerToYaml (param.requiredLayer) << "\n";
                            out << indent (4) << "output_layer: "
                                << layerToYaml (param.outputLayer) << "\n";
                            out << indent (4) << "inherits_from_param: "
                                << (param.inheritsFromParam ? "true" : "false")
                                << "\n";
                            out << indent (4)
                                << "inherit_source: " << param.inheritSource
                                << "\n";
                            out << indent (4) << "mod_status: "
                                << modStatusToYaml (param.modStatus) << "\n";
                        }
                }

            // Write call sites if present (provenance data)
            if (!func.callSites.empty ())
                {
                    out << indent (2) << "call_sites:\n";
                    for (const auto &cs : func.callSites)
                        {
                            out << indent (3) << "- callee: " << cs.calleeName
                                << "\n";
                            out << indent (4) << "location: \"" << cs.location
                                << "\"\n";
                            if (!cs.bindings.empty ())
                                {
                                    out << indent (4) << "bindings:\n";
                                    for (const auto &b : cs.bindings)
                                        {
                                            out << indent (5)
                                                << "- arg_index: " << b.argIndex
                                                << "\n";
                                            out << indent (6)
                                                << "from_caller_param: "
                                                << (b.fromCallerParam ? "true"
                                                                      : "false")
                                                << "\n";
                                            if (b.fromCallerParam)
                                                {
                                                    out << indent (6)
                                                        << "caller_param_index:"
                                                           " "
                                                        << b.callerParamIndex
                                                        << "\n";
                                                    out << indent (6)
                                                        << "is_direct_pass_"
                                                           "through: "
                                                        << (b.isDirectPassThrough
                                                                ? "true"
                                                                : "false")
                                                        << "\n";
                                                }
                                            if (!b.sourceName.empty ())
                                                {
                                                    out << indent (6)
                                                        << "source_name: \""
                                                        << b.sourceName
                                                        << "\"\n";
                                                }
                                        }
                                }
                        }
                }
        }

    out << "...\n";
    out.close ();
    return true;
}

