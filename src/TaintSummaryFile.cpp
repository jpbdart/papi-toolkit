TaintLayer
SummaryFileReader::parseLayer (const std::string &str)
{
    std::string s = trim (str);
    if (s == "RAW")
        return TaintLayer::RAW;
    if (s == "SYNTACTIC")
        return TaintLayer::SYNTACTIC;
    if (s == "SEMANTIC")
        return TaintLayer::SEMANTIC;
    if (s == "CONTEXTUAL")
        return TaintLayer::CONTEXTUAL;
    if (s == "CLEAN")
        return TaintLayer::CLEAN;
    return TaintLayer::RAW; // Default
}

ParamDirection
SummaryFileReader::parseDirection (const std::string &str)
{
    std::string s = trim (str);
    if (s == "IN")
        return ParamDirection::IN;
    if (s == "OUT")
        return ParamDirection::OUT;
    if (s == "INOUT")
        return ParamDirection::INOUT;
    return ParamDirection::IN; // Default
}

ParamModStatus
parseModStatus (const std::string &str)
{
    std::string s = str;
    // Trim
    size_t start = s.find_first_not_of (" \t\n\r");
    if (start != std::string::npos)
        {
            size_t end = s.find_last_not_of (" \t\n\r");
            s = s.substr (start, end - start + 1);
        }
    if (s == "PASS_THROUGH")
        return ParamModStatus::PASS_THROUGH;
    if (s == "MODIFIED")
        return ParamModStatus::MODIFIED;
    return ParamModStatus::UNKNOWN;
}

bool
SummaryFileReader::read (const std::string &filename,
                         std::vector<FunctionSummary> &summaries)
{
    std::ifstream in (filename);
    if (!in.is_open ())
        {
            llvm::errs () << "Error: Cannot open file for reading: " << filename
                          << "\n";
            return false;
        }

    summaries.clear ();

    std::string line;
    FunctionSummary currentFunc;
    ParamSummary currentParam;
    bool inFunction = false;
    bool inParams = false;
    bool inParam = false;

    enum class State
    {
        TOP,
        FUNCTIONS,
        FUNCTION,
        PARAMS,
        PARAM
    };
    State state = State::TOP;

    while (std::getline (in, line))
        {
            std::string trimmed = trim (line);

            // Skip comments and empty lines
            if (trimmed.empty () || trimmed[0] == '#' || trimmed == "---"
                || trimmed == "...")
                {
                    continue;
                }

            if (trimmed.starts_with("version:"))
                {
                    // Version check could go here
                    continue;
                }

            if (trimmed.starts_with("source_file:"))
                {
                    // Could store source file
                    continue;
                }

            if (trimmed.starts_with("functions:"))
                {
                    state = State::FUNCTIONS;
                    continue;
                }

            if (state == State::FUNCTIONS || state == State::FUNCTION
                || state == State::PARAMS || state == State::PARAM)
                {

                    // New function entry
                    if (trimmed.find ("- name:") == 0)
                        {
                            // Save previous function if exists
                            if (inFunction)
                                summaries.push_back (currentFunc);

                            // Start new function
                            currentFunc = FunctionSummary ();
                            currentFunc.name = getValue (trimmed);
                            inFunction = true;
                            inParams = false;
                            inParam = false;
                            state = State::FUNCTION;
                            continue;
                        }

                    // New param entry
                    if (trimmed.find ("- index:") == 0)
                        {
                            // Save previous param if exists
                            if (inParam)
                                {
                                    currentFunc.params.push_back (currentParam);
                                }

                            // Start new param
                            currentParam = ParamSummary ();
                            currentParam.index = std::stoi (getValue (trimmed));
                            inParam = true;
                            state = State::PARAM;
                            continue;
                        }

                    // Function fields
                    if (state == State::FUNCTION && !inParams)
                        {
                            if (trimmed.starts_with("qualified_name:"))
                                {
                                    currentFunc.qualifiedName
                                        = getValue (trimmed);
                                }
                            else if (trimmed.starts_with("return_layer:"))
                                {
                                    currentFunc.returnLayer
                                        = parseLayer (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("return_inherits:"))
                                {
                                    currentFunc.returnInherits
                                        = (getValue (trimmed) == "true");
                                }
                            else if (trimmed.starts_with("return_inherit_source:"))
                                {
                                    currentFunc.returnInheritSource
                                        = std::stoi (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("is_taint_source:"))
                                {
                                    currentFunc.isTaintSource
                                        = (getValue (trimmed) == "true");
                                }
                            else if (trimmed.starts_with("is_taint_sink:"))
                                {
                                    currentFunc.isTaintSink
                                        = (getValue (trimmed) == "true");
                                }
                            else if (trimmed.starts_with("sink_requirement:"))
                                {
                                    currentFunc.sinkRequirement
                                        = parseLayer (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("params:"))
                                {
                                    inParams = true;
                                    state = State::PARAMS;
                                }
                        }

                    // Param fields
                    if (state == State::PARAM && inParam)
                        {
                            if (trimmed.starts_with("name:"))
                                {
                                    currentParam.name = getValue (trimmed);
                                }
                            else if (trimmed.starts_with("direction:"))
                                {
                                    currentParam.direction
                                        = parseDirection (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("required_layer:"))
                                {
                                    currentParam.requiredLayer
                                        = parseLayer (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("output_layer:"))
                                {
                                    currentParam.outputLayer
                                        = parseLayer (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("inherits_from_param:"))
                                {
                                    currentParam.inheritsFromParam
                                        = (getValue (trimmed) == "true");
                                }
                            else if (trimmed.starts_with("inherit_source:"))
                                {
                                    currentParam.inheritSource
                                        = std::stoi (getValue (trimmed));
                                }
                            else if (trimmed.starts_with("mod_status:"))
                                {
                                    currentParam.modStatus
                                        = parseModStatus (getValue (trimmed));
                                }
                        }
                }
        }

    // Save last function and param
    if (inParam)
        {
            currentFunc.params.push_back (currentParam);
        }
    if (inFunction)
        {
            // Rebuild the pass-through/modified sets from params
            currentFunc.rebuildParamSets ();
            summaries.push_back (currentFunc);
        }

    in.close ();
    return true;
}

bool
SummaryFileReader::loadIntoDatabase (const std::string &filename,
                                     FunctionDatabase &db)
{
    std::vector<FunctionSummary> summaries;
    if (!read (filename, summaries))
        {
            return false;
        }

    for (const auto &summary : summaries)
        {
            db.addSummary (summary);
        }

    llvm::errs () << "Loaded " << summaries.size () << " summaries from "
                  << filename << "\n";
    return true;
}

bool
SummaryFileReader::loadDirectory (const std::string &directory,
                                  FunctionDatabase &db)
{
    namespace fs = std::filesystem;

    if (!fs::exists (directory) || !fs::is_directory (directory))
        {
            return false;
        }

    int count = 0;
    for (const auto &entry : fs::directory_iterator (directory))
        {
            if (entry.path ().extension () == ".taint")
                {
                    if (loadIntoDatabase (entry.path ().string (), db))
                        {
                            count++;
                        }
                }
        }

    llvm::errs () << "Loaded summaries from " << count << " files in "
                  << directory << "\n";
    return count > 0;
}

