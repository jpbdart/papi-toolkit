SummaryManager::SummaryManager (FunctionDatabase &db) : db_ (db) {}

std::string
SummaryManager::getSummaryPath (const std::string &sourceFile)
{
    namespace fs = std::filesystem;

    fs::path source (sourceFile);
    std::string basename = source.stem ().string ();
    return basename + ".taint";
}

bool
SummaryManager::saveSummaries (const std::string &sourceFile,
                               const std::vector<FunctionSummary> &summaries)
{
    namespace fs = std::filesystem;

    // Create summary directory if it doesn't exist
    if (!fs::exists (summaryDir_))
        {
            fs::create_directories (summaryDir_);
        }

    std::string summaryFile = summaryDir_ + "/" + getSummaryPath (sourceFile);
    return SummaryFileWriter::write (summaryFile, summaries, sourceFile);
}

bool
SummaryManager::loadSummaries (const std::string &directory)
{
    return SummaryFileReader::loadDirectory (directory, db_);
}

